// ========== TF-IDF 分类引擎 ==========

interface CategoryScore {
  category: string
  score: number
}

// 编辑距离算法（Levenshtein Distance）
function levenshtein(a: string, b: string): number {
  const m = a.length
  const n = b.length
  
  // 创建二维数组
  const dp: number[][] = Array(m + 1).fill(null).map(() => Array(n + 1).fill(0))
  
  // 初始化
  for (let i = 0; i <= m; i++) dp[i][0] = i
  for (let j = 0; j <= n; j++) dp[0][j] = j
  
  // 填充
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      if (a[i - 1] === b[j - 1]) {
        dp[i][j] = dp[i - 1][j - 1]
      } else {
        dp[i][j] = Math.min(
          dp[i - 1][j] + 1,     // 删除
          dp[i][j - 1] + 1,     // 插入
          dp[i - 1][j - 1] + 1  // 替换
        )
      }
    }
  }
  
  return dp[m][n]
}

// 模糊匹配：找到最接近的关键词
function findClosestKeyword(text: string, keywords: string[]): { keyword: string; distance: number } | null {
  let closest = null
  let minDistance = Infinity
  
  for (const keyword of keywords) {
    const kw = keyword.toLowerCase()
    const textLower = text.toLowerCase()
    
    // 方法1：直接比较整个文本
    const dist1 = levenshtein(textLower, kw)
    const maxAllowed1 = kw.length <= 3 ? 1 : 2
    if (dist1 < minDistance && dist1 <= maxAllowed1) {
      minDistance = dist1
      closest = keyword
    }
    
    // 方法2：在文本中查找与关键词长度相近的子串
    const kwLen = kw.length
    for (let len = kwLen - 1; len <= kwLen + 2; len++) {
      for (let i = 0; i <= textLower.length - len; i++) {
        const substring = textLower.substring(i, i + len)
        const dist2 = levenshtein(substring, kw)
        const maxAllowed2 = kwLen <= 3 ? 1 : 2
        if (dist2 < minDistance && dist2 <= maxAllowed2) {
          minDistance = dist2
          closest = keyword
        }
      }
    }
  }
  
  return closest ? { keyword: closest, distance: minDistance } : null
}

// 词频统计（TF）
function tokenize(text: string): string[] {
  const lower = text.toLowerCase()
  const tokens: string[] = []
  
  // 中文：按字和词切分
  for (let i = 0; i < lower.length; i++) {
    tokens.push(lower[i])
    if (i < lower.length - 1) {
      tokens.push(lower.substring(i, i + 2))
    }
    if (i < lower.length - 2) {
      tokens.push(lower.substring(i, i + 3))
    }
  }
  
  return tokens
}

// 计算文本与训练数据的相似度
function computeSimilarity(text: string, keywords: string[]): number {
  const lower = text.toLowerCase()
  let score = 0
  
  for (const keyword of keywords) {
    const kw = keyword.toLowerCase()
    if (lower.includes(kw)) {
      // 精确匹配：长关键词权重更高
      score += kw.length * kw.length
    } else {
      // 模糊匹配：尝试编辑距离
      const fuzzy = findClosestKeyword(lower, [keyword])
      if (fuzzy) {
        // 模糊匹配权重较低（精确匹配的一半）
        score += (kw.length * kw.length) * 0.5
        console.log(`[AI分类] 模糊匹配: "${lower}" → "${fuzzy.keyword}" (距离=${fuzzy.distance})`)
      }
    }
  }
  
  return score
}

// 用户反馈权重（从 localStorage 读取）
function getFeedbackWeights(): Record<string, number> {
  try {
    return JSON.parse(localStorage.getItem('category_feedback') || '{}')
  } catch {
    return {}
  }
}

// 保存用户反馈
function saveFeedback(text: string, correctCategory: string) {
  const feedback = getFeedbackWeights()
  const key = `${text}|${correctCategory}`
  feedback[key] = (feedback[key] || 0) + 1
  localStorage.setItem('category_feedback', JSON.stringify(feedback))
}

// 主分类函数
export function classify(text: string): CategoryScore[] {
  const scores: CategoryScore[] = []
  const feedback = getFeedbackWeights()
  
  for (const [category, keywords] of Object.entries(trainingData)) {
    let score = computeSimilarity(text, keywords)
    
    // 应用用户反馈权重
    const feedbackKey = `${text}|${category}`
    if (feedback[feedbackKey]) {
      score += feedback[feedbackKey] * 10
    }
    
    scores.push({ category, score })
  }
  
  // 按分数排序
  scores.sort((a, b) => b.score - a.score)
  
  return scores
}

// 获取最佳分类
export function classifyTop(text: string): string {
  const scores = classify(text)
  if (scores[0].score === 0) return '其他'
  return scores[0].category
}

// 提交用户纠正（反馈学习）
export function submitCorrection(text: string, correctCategory: string) {
  saveFeedback(text, correctCategory)
  console.log(`[AI分类] 用户纠正: "${text}" → ${correctCategory}`)
}

// 获取训练统计
export function getTrainingStats() {
  const feedback = getFeedbackWeights()
  const feedbackCount = Object.keys(feedback).length
  const totalSamples = Object.values(trainingData).reduce((sum, arr) => sum + arr.length, 0)
  
  return {
    totalSamples,
    feedbackCount,
    categories: Object.keys(trainingData).length,
    categorySamples: Object.entries(trainingData).map(([cat, samples]) => ({
      category: cat,
      count: samples.length,
    })),
  }
}

export default { classify, classifyTop, submitCorrection, getTrainingStats }
